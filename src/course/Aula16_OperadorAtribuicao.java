package course;

public class Aula16_OperadorAtribuicao {

	public static void main(String[] args) {
		
		int a = 10;
		int b = 10;
		
		System.out.println(a++); // Vai printar o valor antes e incrementar o valor depois.
		System.out.println(a); // Valor já incrementado.
		
		System.out.println(++b); // Primeiro o valor é incrementado para depois ser printado.

	}

}
